# Функция для создания профиля "процветания" одной династии
def dynasty_wave(years, start, end, peak_power=1.0):
    """
    Генерирует массив процветания для династии.
    Использует логистическую кривую и плавный спад.
    """
    power = np.zeros_like(years, dtype=float)

    # Индексы начала и конца династии во временной шкале
    start_idx = np.searchsorted(years, start)
    end_idx = np.searchsorted(years, end)

    if start_idx >= len(years) or end_idx <= 0 or start_idx >= end_idx:
        return power

    # Длина династии в годах
    reign_length = end - start

    # Точка пика (обычно первая треть правления)
    peak_year = start + reign_length * 0.25
    peak_idx = np.searchsorted(years, peak_year)

    # Период жизни династии
    reign_indices = range(start_idx, min(end_idx, len(years)))

    for i in reign_indices:
        t = years[i] - start
        # Форма волны: быстрый подъем, плато, медленный спад
        if i <= peak_idx:
            # Подъем (сигмоида)
            # Экспоненциальный рост, но не резкий
            progress = (i - start_idx) / max(1, (peak_idx - start_idx))
            power[i] = peak_power * (1 - np.exp(-5 * progress))  # Растущая экспонента
        else:
            # Спад (медленное затухание)
            decay_progress = (i - peak_idx) / max(1, (end_idx - peak_idx))
            # Используем exp(-x^2) для плавного спада, который ускоряется к концу
            power[i] = peak_power * np.exp(-2 * decay_progress ** 2)

    # Небольшой шум для реализма (опционально)
    # noise = np.random.normal(0, 0.02, len(power))
    # power += noise

    return power


